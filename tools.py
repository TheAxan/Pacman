def unreachable_finder(array, start_node):
    nodes_to_explore = queue.Queue()
    explored_nodes = set()
    nodes_to_explore.put(start_node)

    while not nodes_to_explore.empty():
        current_node = nodes_to_explore.get()
        for new_node in pathing.neighbors(current_node, array, (1, 3)):
            if new_node not in explored_nodes:
                nodes_to_explore.put(new_node)
            explored_nodes.add(current_node)

    return [
        [
            4 if (x, y) not in explored_nodes and i not in (1, 3)
            else i
            for x, i in enumerate(row)
        ]
        for y, row in enumerate(array)
    ]


def turn_finder(array):
    new_map = []
    for y_counter, row in enumerate(array):
        new_row = []
        for x_counter, cell in enumerate(row):
            if cell in (1, 3, 4):
                new_row.append(cell)
            else:
                try:
                    if x_counter - 1 < 0 or y_counter -1 < 0:  # exception in case of future asymetric tunnels
                        raise Exception('negative index')
                    
                    up = not array[y_counter - 1][x_counter]
                    left = not array[y_counter][x_counter - 1]
                    right = not array[y_counter][x_counter + 1]
                    down = not array[y_counter + 1][x_counter]

                    if (left and (up or down)) or (right and (up or down)):
                        new_row.append(2)
                    else:
                        new_row.append(0)
                except:
                    new_row.append(0)
        new_map.append(new_row)
    return new_map


